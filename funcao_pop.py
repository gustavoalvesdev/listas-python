# Criando uma lista de números
numeros = [10, 20, 30, 40, 50]

# Removendo e retornando o último elemento
ultimo = numeros.pop()
print(ultimo)
print(numeros)

# É possível remover um elemento num índice específico, passando o índice como parÂmetro
segundo = numeros.pop(1)
print(segundo)
print(numeros)